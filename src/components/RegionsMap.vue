<script>
import { directive } from "vue-tippy";

export default {
  name: "RegionsMap",

  props: {
    regions: { type: Object, default: () => {} }
  },

  directives: { tippy: directive },

  data() {
    return {
      show: false
    };
  },

  methods: {
    exists(region) {
      return this.regions[region];
    },

    time(region) {
      return (
        (this.regions[region].ttfb / this.regions[region].qty).toFixed(2) +
        "ms."
      );
    },

    getClass(regionName) {
      const region = this.regions[regionName];
      if (!region) {
        return "";
      }

      let className = "on ";

      if (region.ttfb / region.qty <= 200) {
        return className + "fill-green";
      }

      if (region.ttfb / region.qty <= 500) {
        return className + "fill-yellow";
      }

      return className + "fill-red";
    }
  }
};
